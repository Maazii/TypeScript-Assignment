function city_country(city, country) {
    return "".concat(city, ", ").concat(country);
}
var location1 = city_country("Ziarat", "Pakistan");
var location2 = city_country("Nagasaki", "Japan");
var location3 = city_country("Marsielle", "France");
console.log(location1);
console.log(location2);
